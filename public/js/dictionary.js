wordCloud.forEach((word) => {
  for (let num = 0; num < word.p; num++) {
    dictionary.push(word.word)
  }
});

let displayedWords = [];

function randomWord() {
  let randomNum = Math.floor(Math.random() * dictionary.length);
  let word = dictionary[randomNum];

  while (displayedWords.includes(word)) {
    randomNum = Math.floor(Math.random() * dictionary.length);
    word = dictionary[randomNum];
  }

  return word;
}